static void arc_loader(lv_task_t *t)
{
  uint16_t end_angle = adc/10;
  if (end_angle >= 360) {
      end_angle = 360;
  } else if (end_angle <= 0){
    end_angle = 1;
  }
  
  lv_arc_set_end_angle(t->user_data, end_angle);
}

static void create_arc()
{
  /*Create an Arc*/
  lv_obj_t * arc = lv_arc_create(lv_scr_act(), NULL);
  lv_arc_set_bg_angles(arc, 0, 360);
  lv_arc_set_angles(arc, 0, 359);
  lv_obj_align(arc, NULL, LV_ALIGN_CENTER, 0, 15);

  /* Create an `lv_task` to update the arc.
   * Store the `arc` in the user data*/
  lv_task_create(arc_loader, 20, LV_TASK_PRIO_LOWEST, arc);
}

